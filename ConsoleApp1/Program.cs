//引入命名空间
using System; 

//命名空间
namespace ConsoleApp1
{
    //类
    class Program
    {
        public static void Method() {
            Console.WriteLine("I am qiyunkong");
        }

        //Main 方法是程序的入口点
        static void Main(string[] args)
        {
            Program.Method();
            Console.WriteLine("Hello World!");
        }
    }
}


